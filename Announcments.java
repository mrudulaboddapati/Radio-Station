package com.playlist.pojo;

public class Announcments {

	private int annoucment_Id;
	private int totaltime;
	private String name;
	private String type;

	public int getAnnoucment_Id() {
		return annoucment_Id;
	}

	public void setAnnoucment_Id(int annoucment_Id) {
		this.annoucment_Id = annoucment_Id;
	}

	public int getTotaltime() {
		return totaltime;
	}

	public void setTotaltime(int totaltime) {
		this.totaltime = totaltime;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

}
